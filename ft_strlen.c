#include "libft.h"

size_t			ft_strlen(const char *s)
{
	size_t		i;
	const char	*ptr;

	i = 0;
	ptr = s;
	while (*ptr)
	{
		i += 1;
		ptr = ptr + 1;
	}
	return (i);
}
