#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int neg;
	int nb;
	int counter;

	neg = 1;
	counter = 0;
	i = 0;
	nb = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+')
		i++;
	if (str[i] == '-')
	{
		neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
		counter++;
	}
	return (neg * nb);
}
